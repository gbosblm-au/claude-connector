export const clientRegistryUpdateToolDefinition = {
  name: 'client_registry_update',
  description:
    'Add, update, or remove a client from the peer review registry. ' +
    'The registry stores the gateway URL, tenant ID, and peer review key for each enrolled client. ' +
    'Use action=add when first enrolling a client. Use action=update to rotate a key or change details. ' +
    'Use action=remove to unenroll a client. ' +
    'The peer review key is generated via the gateway admin endpoint ' +
    '(POST /admin/peer-review/{tenant_id}/generate-key) and shown once only. ' +
    'Only available in owner-mode connectors.',
  inputSchema: {
    type: 'object',
    properties: {
      action: {
        type: 'string',
        enum: [ 'add', 'update', 'remove' ],
        description: 'add = enrol new client. update = change details. remove = unenrol.',
      },
      client_name: {
        type: 'string',
        description: 'Display name of the client, e.g. "Smith Partners".',
      },
      tenant_id: {
        type: 'string',
        description: 'Tenant ID slug as registered in the gateway, e.g. "smith-partners".',
      },
      gateway_url: {
        type: 'string',
        description: 'Base URL of the TrueSource Gateway Service, e.g. "https://gateway.railway.app".',
      },
      peer_review_key: {
        type: 'string',
        description:
          'Plain peer review read key generated by the gateway admin endpoint. ' +
          'Required for add/update. Not needed for remove.',
      },
      notes: {
        type: 'string',
        description: 'Optional internal notes about this client.',
      },
    },
    required: [ 'action', 'tenant_id' ],
  },
};

export async function handleClientRegistryUpdate( args ) {
  const action          = ( args.action      || '' ).toLowerCase();
  const tenant_id       = String( args.tenant_id       || '' ).trim();
  const client_name     = String( args.client_name     || tenant_id ).trim();
  const gateway_url     = String( args.gateway_url     || '' ).replace( /\/$/, '' ).trim();
  const peer_review_key = String( args.peer_review_key || '' ).trim();
  const notes           = String( args.notes           || '' ).trim();

  if ( ! tenant_id ) {
    return errResult( 'tenant_id is required.' );
  }

  const valid_actions = new Set( [ 'add', 'update', 'remove' ] );
  if ( ! valid_actions.has( action ) ) {
    return errResult( 'action must be add, update, or remove.' );
  }

  if ( action === 'remove' ) {
    return {
      content: [ {
        type: 'text',
        text: JSON.stringify( {
          registry_action: 'remove',
          tenant_id,
          memory_key:      `client_${ tenant_id }`,
          category:        'peer_review_registry',
          instruction:     'Call memory_delete with category=peer_review_registry and key=client_' + tenant_id + ' to complete removal.',
        }, null, 2 ),
      } ],
    };
  }

  if ( ! gateway_url ) {
    return errResult( 'gateway_url is required for add/update.' );
  }
  if ( ! peer_review_key ) {
    return errResult( 'peer_review_key is required for add/update.' );
  }

  const entry = {
    client_name,
    tenant_id,
    gateway_url,
    peer_review_key,
    notes,
    enrolled_at: new Date().toISOString(),
  };

  return {
    content: [ {
      type: 'text',
      text: JSON.stringify( {
        registry_action: action,
        tenant_id,
        memory_category: 'peer_review_registry',
        memory_key:      `client_${ tenant_id }`,
        memory_value:    JSON.stringify( entry ),
        instruction:
          `Call memory_write with category=peer_review_registry, ` +
          `key=client_${ tenant_id }, and the memory_value above to save this entry.`,
        entry,
      }, null, 2 ),
    } ],
  };
}

