export async function handleEscalationQueueRead( args ) {
  const clear_after = args.clear_after_read === true;

  const instructions = [
    'Call memory_read with category=peer_review_escalations, key=queue to retrieve the current escalation list.',
    'Call memory_read with category=peer_review_logs to see recent check-in history across all clients.',
  ];

  if ( clear_after ) {
    instructions.push(
      'After reviewing, call memory_delete with category=peer_review_escalations, key=queue to clear actioned escalations.'
    );
  }

  return {
    content: [ {
      type: 'text',
      text: JSON.stringify( {
        queue_category: 'peer_review_escalations',
        queue_key:      'queue',
        logs_category:  'peer_review_logs',
        clear_after_read: clear_after,
        instructions,
        note: 'Use the memory_read tool with the category and key above to retrieve the actual queue content.',
      }, null, 2 ),
    } ],
  };
}

