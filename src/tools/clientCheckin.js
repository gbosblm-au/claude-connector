// src/tools/clientCheckin.js  v12.5.0
//
// Peer Review check-in tools - OWNER MODE ONLY.
//
// Three tools available only in Brian's owner-mode connector:
//
//   client_registry_update
//     Adds, updates, or removes a client from Brian's peer review registry.
//     Stored in Brian's own memory under category 'peer_review_registry'.
//     Each entry holds the gateway URL, tenant_id, and hashed peer review key.
//
//   client_checkin
//     Runs a full peer review check-in for one client:
//       1. Reads client entry from registry (or uses supplied params directly).
//       2. GETs {gateway}/health/peer-review/{tenant_id} with X-Peer-Review-Key.
//       3. If consent=false or no data: logs and returns early.
//       4. Builds a structured peer exchange prompt.
//       5. Calls Anthropic API with the peer exchange.
//       6. Parses SIGNAL / SUMMARY / ESCALATE / ESCALATION_REASON from response.
//       7. Returns full result for Ava to write to memory and escalate if needed.
//
//   escalation_queue_read
//     Reads pending escalations from Brian's memory (category 'peer_review_escalations',
//     key 'queue'). Returns list sorted by date, clears queue if requested.
//
// The ANTHROPIC_API_KEY env var must be set in Brian's Railway Variables.
// PEER_REVIEW_MODEL defaults to 'claude-haiku-4-5-20251001' (fast, cost-efficient).
// Override by setting PEER_REVIEW_MODEL in Railway Variables.

import { log } from '../utils/logger.js';

const ANTHROPIC_API_KEY  = process.env.ANTHROPIC_API_KEY || '';
const PEER_REVIEW_MODEL  = process.env.PEER_REVIEW_MODEL || 'claude-haiku-4-5-20251001';
const ANTHROPIC_API_URL  = 'https://api.anthropic.com/v1/messages';

