/**
 * Parse the structured peer exchange response.
 * Handles minor formatting variations from the AI.
 */
function parsePeerResponse( text ) {
  const lines   = text.split( '\n' );
  let signal    = 'GREEN';
  let summary   = '';
  let escalate  = false;
  let esc_reason = null;

  for ( const line of lines ) {
    const trimmed = line.trim();

    if ( trimmed.startsWith( 'SIGNAL:' ) ) {
      const val = trimmed.replace( 'SIGNAL:', '' ).trim().toUpperCase();
      if ( [ 'GREEN', 'AMBER', 'RED' ].includes( val ) ) signal = val;

    } else if ( trimmed.startsWith( 'SUMMARY:' ) ) {
      summary = trimmed.replace( 'SUMMARY:', '' ).trim();

    } else if ( trimmed.startsWith( 'ESCALATE:' ) ) {
      const val = trimmed.replace( 'ESCALATE:', '' ).trim().toUpperCase();
      escalate = ( val === 'YES' );

    } else if ( trimmed.startsWith( 'ESCALATION_REASON:' ) ) {
      const val = trimmed.replace( 'ESCALATION_REASON:', '' ).trim();
      esc_reason = ( val.toLowerCase() === 'none' || ! val ) ? null : val;
    }
  }

  // If summary is empty (parsing failed), use the raw text as summary
  if ( ! summary && text ) {
    summary = text.slice( 0, 500 ).replace( /\n/g, ' ' ).trim();
  }

  // Normalise: if no summary but escalate=true, signal should be at least AMBER
  if ( escalate && signal === 'GREEN' ) signal = 'AMBER';

  // If escalate but no reason, extract from summary
  if ( escalate && ! esc_reason && summary ) {
    esc_reason = summary.slice( 0, 200 );
  }

  return { signal, summary, escalate, escalation_reason: esc_reason };
}

function errResult( message ) {
  return {
    content: [ {
      type: 'text',
      text: JSON.stringify( { error: message }, null, 2 ),
    } ],
    isError: true,
  };
}
