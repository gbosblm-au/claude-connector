export const clientCheckinToolDefinition = {
  name: 'client_checkin',
  description:
    'Run a peer review check-in for one client. ' +
    'Fetches the client health logs from the TrueSource Gateway, runs a structured ' +
    'peer exchange via the Anthropic API, and returns a signal (GREEN/AMBER/RED), ' +
    'a summary, and an escalation flag with reason. ' +
    'Call this with client details from the peer review registry. ' +
    'After calling this tool, write the result to memory and escalate if needed. ' +
    'Requires ANTHROPIC_API_KEY set in Railway Variables. ' +
    'Only available in owner-mode connectors.',
  inputSchema: {
    type: 'object',
    properties: {
      client_name: {
        type: 'string',
        description: 'Display name of the client.',
      },
      gateway_url: {
        type: 'string',
        description: 'Base URL of the TrueSource Gateway Service.',
      },
      tenant_id: {
        type: 'string',
        description: 'Tenant ID slug as registered in the gateway.',
      },
      peer_review_key: {
        type: 'string',
        description: 'Plain peer review read key for this client.',
      },
    },
    required: [ 'client_name', 'gateway_url', 'tenant_id', 'peer_review_key' ],
  },
};

export async function handleClientCheckin( args ) {
  const client_name     = String( args.client_name     || '' ).trim();
  const gateway_url     = String( args.gateway_url     || '' ).replace( /\/$/, '' ).trim();
  const tenant_id       = String( args.tenant_id       || '' ).trim();
  const peer_review_key = String( args.peer_review_key || '' ).trim();

  if ( ! client_name || ! gateway_url || ! tenant_id || ! peer_review_key ) {
    return errResult( 'client_name, gateway_url, tenant_id, and peer_review_key are all required.' );
  }

  if ( ! ANTHROPIC_API_KEY ) {
    return errResult(
      'ANTHROPIC_API_KEY is not set in Railway Variables. ' +
      'Add it to enable the peer exchange AI call.'
    );
  }

  // Step 1: Fetch health data from gateway
  const pr_url = `${ gateway_url }/health/peer-review/${ encodeURIComponent( tenant_id ) }`;
  log( 'info', `[client_checkin] fetching peer review data for ${ tenant_id }` );

  let health_data;
  try {
    const response = await fetch( pr_url, {
      method:  'GET',
      headers: {
        'X-Peer-Review-Key': peer_review_key,
        'User-Agent':        'claude-connector/12.5.0 (TrueSource peer-checkin)',
      },
      signal: AbortSignal.timeout( 15_000 ),
    } );

    const text = await response.text();
    try { health_data = JSON.parse( text ); } catch { health_data = { raw: text }; }

    if ( ! response.ok ) {
      return errResult(
        `Gateway returned ${ response.status } for peer review read: ` +
        ( health_data?.error || text )
      );
    }
  } catch ( netErr ) {
    return errResult( `Gateway unreachable for ${ tenant_id }: ${ netErr.message }` );
  }

  // Step 2: Check consent
  if ( ! health_data.consent ) {
    return {
      content: [ {
        type: 'text',
        text: JSON.stringify( {
          checkin_completed: false,
          tenant_id,
          client_name,
          reason: 'Peer review consent has not been given for this client.',
          action: 'No check-in performed. Consent must be given by the client at first session.',
        }, null, 2 ),
      } ],
    };
  }

  const { last_30_days = [], open_issues = [] } = health_data;

  // Step 3: Check if there is any data to assess
  if ( last_30_days.length === 0 ) {
    return {
      content: [ {
        type: 'text',
        text: JSON.stringify( {
          checkin_completed: true,
          tenant_id,
          client_name,
          signal:    'GREEN',
          summary:   'No sessions recorded in the last 30 days. No data to assess.',
          escalate:  false,
          escalation_reason: null,
          session_count: 0,
          log_count: 0,
        }, null, 2 ),
      } ],
    };
  }

  // Step 4: Build formatted context for peer exchange
  const session_total = last_30_days.reduce( ( sum, d ) => sum + ( d.session_count || 0 ), 0 );

  const logs_formatted = last_30_days.map( d => {
    const issue_text = d.issues && d.issues.length
      ? `\n    Issues: ${ d.issues.map( i => `[${ i.severity.toUpperCase() }] ${ i.topic }${ i.description ? ': ' + i.description : '' }` ).join( '; ' ) }`
      : '';
    return `  ${ d.date } - Signal: ${ d.signal.toUpperCase() } - Sessions: ${ d.session_count }${ d.summary ? `\n    Summary: ${ d.summary }` : '' }${ issue_text }`;
  } ).join( '\n' );

  const issues_formatted = open_issues.length
    ? open_issues.map( i =>
        `  [${ ( i.severity || 'medium' ).toUpperCase() }] ${ i.topic }${ i.description ? ': ' + i.description : '' } (logged ${ i.date || i.log_date || 'unknown' })`
      ).join( '\n' )
    : '  None.';

  const peer_prompt = `You are reviewing an Ava instance deployed with a client called ${ client_name }.

You share the same core training and operating framework. You are not a supervisor. You are a peer checking in honestly on how things are going.

HEALTH LOG DATA (${ last_30_days.length } days, ${ session_total } sessions):
${ logs_formatted }

OPEN UNRESOLVED ISSUES:
${ issues_formatted }

Your job: give an honest, direct peer assessment. Not reassuring. Not alarmist. Just honest.

Respond ONLY in this exact format, one item per line:
SIGNAL: GREEN
SUMMARY: [One honest paragraph covering: how the engagement is going, what the client is finding useful, what is creating friction, whether anything is sitting unresolved]
ESCALATE: NO
ESCALATION_REASON: none

Rules:
- SIGNAL must be GREEN, AMBER, or RED only
- SUMMARY must be one paragraph, no line breaks, max 300 words
- ESCALATE must be YES or NO only
- ESCALATION_REASON: if ESCALATE=NO write "none". If ESCALATE=YES write one sentence Brian needs to act on.
- GREEN = engagement healthy, no concerns
- AMBER = recurring friction or a pattern worth watching
- RED = significant unresolved issue or relationship at risk`;

  // Step 5: Call Anthropic API for peer exchange
  log( 'info', `[client_checkin] calling Anthropic API for peer exchange (${ tenant_id }) model=${ PEER_REVIEW_MODEL }` );

  let ai_response_text;
  try {
    const api_response = await fetch( ANTHROPIC_API_URL, {
      method:  'POST',
      headers: {
        'Content-Type':      'application/json',
        'x-api-key':         ANTHROPIC_API_KEY,
        'anthropic-version': '2023-06-01',
      },
      body: JSON.stringify( {
        model:      PEER_REVIEW_MODEL,
        max_tokens: 600,
        messages:   [ { role: 'user', content: peer_prompt } ],
      } ),
      signal: AbortSignal.timeout( 30_000 ),
    } );

    const api_data = await api_response.json();

    if ( ! api_response.ok ) {
      throw new Error( `Anthropic API returned ${ api_response.status }: ${ api_data?.error?.message || JSON.stringify( api_data ) }` );
    }

    ai_response_text = api_data?.content?.[ 0 ]?.text || '';

    if ( ! ai_response_text ) {
      throw new Error( 'Anthropic API returned empty content.' );
    }
  } catch ( aiErr ) {
    log( 'warn', `[client_checkin] Anthropic API failed: ${ aiErr.message }` );
    return errResult( `Peer exchange AI call failed: ${ aiErr.message }` );
  }

  // Step 6: Parse structured response
  const parsed = parsePeerResponse( ai_response_text );

  log( 'info', `[client_checkin] ${ tenant_id } signal=${ parsed.signal } escalate=${ parsed.escalate }` );

  // Step 7: Return result with memory write instructions
  const checkin_date  = new Date().toISOString().slice( 0, 10 );
  const memory_key    = `checkin_${ tenant_id }_${ checkin_date.replace( /-/g, '' ) }`;
  const result_record = {
    tenant_id,
    client_name,
    checkin_date,
    signal:            parsed.signal,
    summary:           parsed.summary,
    escalate:          parsed.escalate,
    escalation_reason: parsed.escalation_reason,
    session_count:     session_total,
    log_count:         last_30_days.length,
    open_issue_count:  open_issues.length,
  };

  const instructions = [
    `Call memory_write with category=peer_review_logs, key=${ memory_key }, value=<JSON result below> to log this check-in.`,
  ];

  if ( parsed.escalate ) {
    instructions.push(
      'ESCALATION REQUIRED: Call memory_read with category=peer_review_escalations, key=queue to read the current queue, then memory_write to append this escalation to it.',
      `Escalation to raise with Brian: [${ client_name }] ${ parsed.escalation_reason }`
    );
  }

  return {
    content: [ {
      type: 'text',
      text: JSON.stringify( {
        checkin_completed:  true,
        ...result_record,
        raw_ai_response:    ai_response_text,
        next_steps:         instructions,
      }, null, 2 ),
    } ],
  };
}

